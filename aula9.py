adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração, subtracao')

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2  # sempre retornará um float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponênciação', exponenciacao)

modulo = 55 % 2  # resta da divisão
print('Módulo', modulo)

print(10 % 8 == 0)  # False, quando o número não é divisível pelo outro
print(16 % 8 == 0)  # True, quando o número é divisível pelo outro
print(10 % 2 == 0)  # Se o número é divisível por 2, True
print(15 % 2 == 0)  # se o número não é divisível por 2, False